    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int confirmacion = JOptionPane.showConfirmDialog (null, "Desea eliminar a este empleado?",FactoryConnectionDb.Mensaje,JOptionPane.YES_NO_OPTION);
        if(JOptionPane.YES_OPTION==confirmacion){
            EmployeeDao productodao = new EmployeeDaoImp();
            productodao.delete(Integer.parseInt(lblId.getText()));
            JOptionPane.showMessageDialog(null, "Empleado eliminado ",FactoryConnectionDb.Mensaje,JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        EmployeeDao empleadodao = new EmployeeDaoImp();
        cEmployee empleado_ = new cEmployee();
        
        String formato = dateC.getDateFormatString();
        SimpleDateFormat sdf = new SimpleDateFormat(formato);        
        Date datE = dateC.getDate();
        String fechaC = String.valueOf(sdf.format(datE));    
        
        if(txtnombre.getText().trim().length()==0){
            JOptionPane.showMessageDialog(null, "Error en el ingreso del nombre",FactoryConnectionDb.Mensaje,JOptionPane.ERROR_MESSAGE);
            txtnombre.requestFocus();        
        }else if(txtapellidoPa.getText().trim().length()==0){
            JOptionPane.showMessageDialog(null, "Error en el ingreso del Apellido paterno",FactoryConnectionDb.Mensaje,JOptionPane.ERROR_MESSAGE);
            txtapellidoPa.requestFocus();
        }else if(txtDNI.getText().trim().length()==0){
            JOptionPane.showMessageDialog(null, "Error en el ingreso del DNI",FactoryConnectionDb.Mensaje,JOptionPane.ERROR_MESSAGE);
            txtDNI.requestFocus();     
        }else{
            empleado_.setIdEmployee(Integer.parseInt(lblId.getText()));
            empleado_.setNameEmployee(txtnombre.getText());
            empleado_.setLast_name(txtapellidoPa.getText());
            empleado_.setBirthdate(fechaC);
            empleado_.setDni(txtDNI.getText());
            empleado_.setSex((rbMas.isSelected())?"m":"f");
            empleado_.setCell_phone(txtcelular.getText());
            empleado_.setPhone(txttelefono.getText());
            empleado_.setEmail(txtcorreo.getText());
            empleado_.setPosition_idPosition(cargoId(comboCargo.getSelectedItem().toString()));
            
            if(empleado_.getIdEmployee()>0){
                empleadodao.update(empleado_);                
                JOptionPane.showMessageDialog(null, "Datos del empleado modificados correctamente",FactoryConnectionDb.Mensaje,JOptionPane.INFORMATION_MESSAGE);
            }                
            else{
                empleadodao.insert(empleado_);
                JOptionPane.showMessageDialog(null, "Datos del empleado ingresados correctamente",FactoryConnectionDb.Mensaje,JOptionPane.INFORMATION_MESSAGE);
            }
            dispose();            
        } 
    }//GEN-LAST:event_btnGrabarActionPerformed


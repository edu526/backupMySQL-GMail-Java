        private String cargo(int idCargo){
            List<cPosition> list = cargodao_.list("");
            o =new Object[2][list.size()];            
            int i = 0;
            for (cPosition tr : list) {   
                o[0][i]=tr.getIdPosition();
                o[1][i]=tr.getNamePosition(); 
                
                if(idCargo == Integer.parseInt(o[0][i].toString()))
                    return  o[1][i].toString(); 
                i++;
            }                
            return "";
        }
        private int cargoId(String nombreCargo){
            List<cPosition> list = cargodao_.list("");
            o =new Object[2][list.size()];            
            int i = 0;
            for (cPosition tr : list) {   
                o[0][i]=tr.getIdPosition();
                o[1][i]=tr.getNamePosition(); 
                
                if(nombreCargo.equals(o[1][i].toString()))
                    return  Integer.parseInt(o[0][i].toString()); 
                i++;
            }                
            return 0;
        }
        
        
